    private void tblDanhSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDanhSachMouseClicked
        // TODO add your handling code here:
        int vitri = tblDanhSach.getSelectedRow();
        int maKH = Integer.parseInt(tblDanhSach.getValueAt(vitri, 0).toString());

        ResultSet rs = KhachHangDAL.LayKHTheoMaKH(maKH);

        JTable tblTam = new JTable();
        KhachHangBLL.LayKHTheoMaKH(rs, tblTam);

        txtTenKH.setText(tblTam.getValueAt(0, 1).toString());
        txtMaKH.setText(tblTam.getValueAt(0, 0).toString());
        txtSDT.setText(tblTam.getValueAt(0, 2).toString());
        txtDiaChi.setText(tblTam.getValueAt(0, 3).toString());

    }//GEN-LAST:event_tblDanhSachMouseClicked

